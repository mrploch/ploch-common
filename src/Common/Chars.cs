namespace Ploch.Common;

/// <summary>
///     Provides a collection of commonly used character constants.
/// </summary>
public static class Chars
{
    /// <summary>
    ///     Represents a space character (' ').
    /// </summary>
    public static readonly char Space = ' ';

    /// <summary>
    ///     Represents an underscore character ('_').
    /// </summary>
    public static readonly char Underscore = '_';

    /// <summary>
    ///     Represents a dash/hyphen character ('-').
    /// </summary>
    public static readonly char Dash = '-';

    /// <summary>
    ///     Represents a dot/period character ('.').
    /// </summary>
    public static readonly char Dot = '.';
}
